        <h2 class="titre">Pourquoi une carte blanche à « titre provisoire » ?</h2>
        <hr width="75%" color="white">
        
        <p class="venter">
                La programmation de Théâtres de Bourbon est cohérente. Le point commun à toutes les 
                troupes et tous les auteurs que nous vous proposons est de s’interroger sur ce qu’est 
                l’homme et penser que le théâtre est le lieu où on peut trouver une réponse à cette question.
                 Nous défendons un théâtre engagé à changer la société en posant des questions qui émeuvent 
                 et qui bouleversent, et nous faisons le pari que nous serons nombreux à venir parce que nous 
                 sommes nombreux à croire dans cette capacité du théâtre, de ce théâtre, à changer l’homme et le monde ! <br><br><br>
                 Avec un tel parti pris, trouver une pièce qui part de l’idée que nous sommes peut-être tous des comédiens
                  qui s'ignorent était une aubaine… En essayant d’illustrer le fait qu’en politique, en amitié, en amour… 
                  partout nous faisons du théâtre, la pièce écrite à quatre mains par Hadrien Brachet et Pauline Mornet, 
                  étudiants à Sciences Po, explore la frontière (souvent très fine) entre performance et réalité et s'interroge 
                  sur l'acte de création théâtrale. Les 15 saynètes qui s’y succèdent, sont autant d’éléments de réponse à 
                  nos interrogations sur ce qu’est l’homme et d’illustrations de notre certitude que le théâtre est le lieu où 
                  on peut trouver une réponse à cette question. Et pour ce qui est de la qualité de l’écriture comme du jeu des 
                  acteurs, il suffit de rappeler que la pièce a été jouée au festival Nuits Sans Entracte 2019 à La Comédie de 
                  Reims où elle a reçu le Prix de la Mise en Scène et le Prix d’Interprétation Masculine.
        </p>
        <br><br><br><br>
       

        <h2 class="titre">Quelques mots sur la troupe 'alterthéâtre'</h2>
        <hr width="75%" color="white">
        
        <p class="venter">
                Tous passionnés de théâtre ils souhaitaient développer un projet qui s’interroge sur la création
                 théâtrale et ce qu’elle révèle de nos vies, de nos comportements et même de notre société. A 
                 travers les thèmes évoqués (politique, famille, relations amoureuses...) le théâtre se révèle
                  être partout, dans nos modes de vie, nos relations aux autres, nos conventions... Ils voulaient
                   ainsi montrer qu’un art que l’on croit inaccessible, élitiste est en réalité au plus près de 
                   notre quotidien, au cœur de nos préoccupations. Les codes du théâtre sont aussi ceux de notre 
                   société et ce sont bien souvent les mêmes règles qui régissent le jeu des acteurs et la vie des 
                   individus. La succession de saynètes ancrées dans des contextes différents leur a semblé un 
                   bon moyen de souligner combien le théâtre, sous toutes ses formes, est une porte d’entrée complète 
                   sur la société. La concordance avec ce que Théâtres de Bourbon mettait dès sa création dans ses 
                   statuts est frappante. Il fallait donc que nous nous rencontrassions.
        </p>
